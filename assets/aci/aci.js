function findByKey(key, val) {
  if (typeof key === 'undefined') {
    throw new Error('invalid key, must be: "aci", "hex" or "rgb"')
  }

  if (typeof val === 'undefined') {
    throw new Error('invalid value, must provide "' + key + '" value')
  }

  let found

  list.forEach(function (item) {
    if (item[key] !== undefined && item[key] === val) {
      found = item
    }
  })

  return found
}

export default {
    list,
    getByACI(aci) {
      if (aci < 0 || aci > 255) throw new Error('invalid ACI')
      return findByKey('aci', aci)
    },
    getByHEX(hex) {
      return findByKey('hex', hex.toUpperCase())
    }
}
